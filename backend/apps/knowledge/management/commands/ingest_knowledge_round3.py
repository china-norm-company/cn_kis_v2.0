class Command(BaseCommand):
    help = '第三轮针对性知识注入（基于 L1 Benchmark v9 分析结果）'

    def add_arguments(self, parser):
        parser.add_argument('--dry-run', action='store_true', help='仅显示将注入的条目，不实际执行')

    def handle(self, *args, **options):
        dry_run = options.get('dry_run', False)
        self.stdout.write(f'第三轮针对性知识注入：共 {len(ROUND3_CORPUS)} 条')
        if dry_run:
            for item in ROUND3_CORPUS:
                self.stdout.write(f'  [DRY] {item.title}')
            return

        ok, fail = 0, 0
        for item in ROUND3_CORPUS:
            try:
                result = run_pipeline(item)
                if result and result.entry_id:
                    entry = KnowledgeEntry.objects.get(id=result.entry_id)
                    entry.is_published = True
                    entry.status = 'published'
                    entry.save(update_fields=['is_published', 'status'])
                    self.stdout.write(self.style.SUCCESS(f'  ✅ #{result.entry_id} {item.title[:50]}'))
                    ok += 1
                else:
                    self.stderr.write(f'  ❌ {item.title[:50]}: pipeline 返回空结果')
                    fail += 1
            except Exception as e:
                self.stderr.write(f'  ❌ {item.title[:50]}: {e}')
                fail += 1

        self.stdout.write(self.style.SUCCESS(f'\n完成：成功 {ok} 条，失败 {fail} 条'))
